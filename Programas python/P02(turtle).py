# Realizamos un cuadrado con instrucciones básicas de tortuga
import turtle

miTortuga = turtle.Turtle()

miTortuga.forward(50)  # Aquí avanzamos 50 pixels
miTortuga.left(90)  # Aquí giramos 90 grados
miTortuga.forward(50)
miTortuga.left(90)
miTortuga.forward(50)
miTortuga.left(90)
miTortuga.forward(50)
